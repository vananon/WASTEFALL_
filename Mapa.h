class ListaDesechable {
private:
	vector<Desechable*>objs;
	ListaGOTASEMILLA* gotasemO;
	int ndesechables1, ndesechables2;
public:
	ListaDesechable(ListaGOTASEMILLA* gotasem) : ndesechables1(0), ndesechables2(0) {
		gotasemO = new ListaGOTASEMILLA(1);
		gotasemO = gotasem;
		for (int i = 0; i < 4; i++) {
			objs.push_back(crear(2));
			dibujar(objs.back()->getForma(), objs.back()->getX(), objs.back()->getY(), 2, 3, 1);
			ndesechables1++;
			objs.push_back(crear(3));
			dibujar(objs.back()->getForma(), objs.back()->getX(), objs.back()->getY(), 2, 3, 1);
			ndesechables2++;
		}
	}

	void EliminarPosi(int ind) {
		ndesechables1 -= (objs[ind]->getColor() == 2);
		ndesechables2 -= (objs[ind]->getColor() == 3);
		objs[ind]-> ~Desechable();
		objs.erase(objs.begin() + ind);
	}
	bool EsColision(int x, int y, int& ind) {
		bool colision = 0;
		for (int i = 0; i < objs.size(); i++) {
			if (x < objs[i]->getX() + 2 && x + 4 > objs[i]->getX() &&
				y < objs[i]->getY() + 3 && y + 4 > objs[i]->getY()) {
				colision = 1; ind = i; break;
			}
		}
		return colision;
	}

	int getColor(int ind) { return objs[ind]->getColor(); }
	int getX(int ind) { return objs[ind]->getX(); }
	int getY(int ind) { return objs[ind]->getY(); }
	int** getForma(int ind) { return objs[ind]->getForma(); }

	Desechable* crear(int color) {
		Desechable* ds;
		int** forma = Matriz(3, 2);
		for (int i = 0; i < 3; i++) {
			for (int j = 0; j < 2; j++) {
				forma[i][j] = MElements[color][i][j];
			}
		}
		int n = color; int x, y;
		bool nocolision = 0;
		while (!nocolision) {
			nocolision = 1;
			x = rand() % (LIMX2 - LIMX1 + 1) + (LIMX1 + 1);
			y = rand() % (LIMY2 - LIMY1 + 1) + (LIMY1 + 1);
			for (Desechable* e : objs) {
				if (x < e->getX() + 6 && x + 6 > e->getX() &&
					y < e->getY() + 6 && y + 6 > e->getY()) {
					nocolision = 0; break;
				}
			}

			nocolision = (nocolision && gotasemO->HayEspacio(x, y));
			//PARA QUE NO SE INICIALIZEN DONDE EL JUGADOR SPAWNEA
			if (x < 19 + 9 && x + 9 > 5 &&
				y < 19 + 6 && y + 6 > 5) {
				nocolision = 0;
			}
		}

		ds = new Desechable(n, x, y, forma);
		return ds;
	}
};

bool  juego() {
	bool siguiente = 1;
	Mapa* map = new Mapa();
	bool tachoexiste = false;
	Guardian* pj = new Guardian();
	ListaGOTASEMILLA* objs = new ListaGOTASEMILLA();
	ListaGrietas* grietas = new ListaGrietas();
	ListaDesechable* ds = new ListaDesechable(objs);
	Enemigos* e1 = new Enemigos(1);
	Enemigos* e2 = new Enemigos(2);
	Tacho* tachito = new Tacho();
	bool mover = true;
	bool quitarvida = 0;
	int cnt = 0;
	char tecla, boton;
	bool colision1 = 0, colision2 = 0, tipo1 = 0, tipo2 = 0;
	int indp, inde1, inde2, aux;
	bool banx1 = 0, banx0 = 0, bany1 = 0, bany0 = 0;
	bool enemigoUnoActivo = false;
	bool enemigoUnoDetenido = false;

	bool enemigoDosActivo = false;
	bool enemigoDosDetenido = false;

	int totalReforestado = 0;
	int enemigoUnoCnt = 0;
	int enemigoDosCnt = 0;
	while (1) {
		if (pj->getvidas() == 0) {
			return 0;
		}
		if (totalReforestado == 7) {
			return 1;
		}
		if (kbhit()) {
			boton = getch();
			switch (boton) {
			case 72:
				if (!bany0) {
					pj->setDY(-1);
					banx0 = banx1 = bany1 = bany0 = 0;
				}
				break;
			case 80:
				if (!bany1) {
					pj->setDY(1);
					banx0 = banx1 = bany1 = bany0 = 0;
				}
				break;
			case 77:
				if (!banx1) {
					pj->setDX(1);
					banx0 = banx1 = bany1 = bany0 = 0;
				}
				break;
			case 75:
				if (!banx0) {
					pj->setDX(-1);
					banx0 = banx1 = bany1 = bany0 = 0;
				}
			}
			cnt = 0;
		}
		if (grietas->verificarSemiColision(pj->getX(), pj->getY(), indp, pj->getDX(), pj->getDY(), banx0, banx1, bany1, bany0)) {
			pj->setDX(0); pj->setDY(0);
			// Verificar recursos
			int semillas = pj->getInventario(0);
			int agua = pj->getInventario(1);
			if (semillas > 0 && agua > 0 && !grietas->verificarEsArbol(indp)) {
				grietas->plantarArbol(indp);
				pj->setInventario2(0);
				map->NumerosPantalla(pj->getInventario(0), 2);

				pj->setInventario2(1);
				map->NumerosPantalla(pj->getInventario(1), 3);

				map->Planto();
				map->NumerosPantalla(map->getNumArboles() * 10, 1);
				mover = false;
				totalReforestado++;
			}
		}
		pj->Mover(cnt);
		if (objs->EsColision(pj->getX(), pj->getY(), indp)) {
			pj->setInventario(objs->getTipo(indp), 1);
			map->NumerosPantalla(pj->getInventario(objs->getTipo(indp)), objs->getTipo(indp) + 2);
			objs->EliminarPosi(indp);
		}
		if (ds->EsColision(pj->getX(), pj->getY(), indp)) {
			pj->setInventario(2, 1);
			map->NumerosPantalla(pj->getInventario(2), 4);
			ds->EliminarPosi(indp);
			if (pj->getInventario(2) == 3 && tachoexiste == false)
			{
				tachito->dibujartacho();
				pj->setInventario2(2);
				map->NumerosPantalla(0, 4);
				tachoexiste = true;
			}
		}
		if (tachito->Colision(pj->getX(), pj->getY(), indp, pj->getDX(), pj->getDY(), banx0, banx1, bany1, bany0))
		{
			pj->setDX(0); pj->setDY(0);
			pj->setInventario2(2);
			map->NumerosPantalla(0, 4);
		}

		//ENEMIGOS---------------------------------------------------------------------------------
		if (pj->getInventario(2) >= 4 && !enemigoUnoDetenido) {
			enemigoUnoActivo = false;
			enemigoUnoDetenido = true;
			e1->borrar(); e1->setX(0); e1->setY(0);
		}

		if (pj->getInventario(2) >= 3 && !enemigoDosDetenido) {
			enemigoDosActivo = false;
			enemigoDosDetenido = true;
			e2->borrar(); e2->setX(0); e2->setY(0);
		}

		if (totalReforestado >= 1 && !enemigoUnoActivo && !enemigoUnoDetenido) {
			enemigoUnoActivo = true;
		}

		if (totalReforestado >= 2 && !enemigoDosActivo && !enemigoDosDetenido) {
			enemigoDosActivo = true;
		}

		if (enemigoUnoActivo) {
			enemigoUnoCnt++;
			e1->movimientoEnUno(enemigoUnoCnt, 1);
		}


		if (enemigoDosActivo) {
			enemigoDosCnt++;
			e2->movimientoEnDos(enemigoDosCnt, 1);
		}

		if (objs->EsColision(e1->getX(), e1->getY(), inde1) || ds->EsColision(e1->getX(), e1->getY(), inde1)) {
			colision1 = 1;
			tipo1 = (ds->EsColision(e1->getX(), e1->getY(), inde1));
		}
		else if (colision1 && (!objs->EsColision(e1->getX(), e1->getY(), aux) && !ds->EsColision(e1->getX(), e1->getY(), aux))) {
			if (tipo1) {
				dibujar(ds->getForma(inde1), ds->getX(inde1), ds->getY(inde1), 2, 3, 1);
			}
			else {
				dibujar(objs->getForma(inde1), objs->getX(inde1), objs->getY(inde1), 2, 3, 1);
			}
			colision1 = 0;
		}


		if (objs->EsColision(e2->getX(), e2->getY(), inde2) || ds->EsColision(e2->getX(), e2->getY(), inde2)) {
			colision2 = 1;
			tipo2 = (ds->EsColision(e2->getX(), e2->getY(), inde2));
		}
		else if (colision2 && (!objs->EsColision(e2->getX(), e2->getY(), aux) && !ds->EsColision(e2->getX(), e2->getY(), aux))) {
			if (tipo2) {
				dibujar(ds->getForma(inde2), ds->getX(inde2), ds->getY(inde2), 2, 3, 1);
			}
			else {
				dibujar(objs->getForma(inde2), objs->getX(inde2), objs->getY(inde2), 2, 3, 1);
			}
			colision2 = 0;
		}
		if (!quitarvida && (Colision(pj->getX(), e1->getX(), pj->getY(), e1->getY()) || Colision(pj->getX(), e2->getX(), pj->getY(), e2->getY()))) {
			quitarvida = 1;

			map->Vida(2, pj->getvidas());
			pj->QuitarVidas();

		}
		else if (quitarvida && (!Colision(pj->getX(), e1->getX(), pj->getY(), e1->getY()) && !Colision(pj->getX(), e2->getX(), pj->getY(), e2->getY()))) {
			quitarvida = 0;
		}
		cnt++;
	}
}